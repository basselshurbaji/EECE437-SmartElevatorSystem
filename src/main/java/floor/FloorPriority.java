package floor;

/**
 * Enum FloorPriority is used to indicate the floor priority.
 */
public enum FloorPriority {
	/**
	 * Specifies that the Floor priority is low
	 */
	LOW,
	/**
	 * Specifies that the Floor priority is medium
	 */
	MEDIUM,
	/**
	 * Specifies that the Floor priority is high
	 */
	HIGH
}
